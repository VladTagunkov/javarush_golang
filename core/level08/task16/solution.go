package main

import "fmt"

func main() {
	var x, newLevel, newMode uint
	fmt.Scan(&x, &newLevel, &newMode)

	// TODO: Достаньте текущие значения полей level и mode из байта x с помощью битовых операций.
	// level — это 3 младших бита.
	// mode — это следующие 2 бита (после level).
	//
	// Важно: нужно именно извлечь поля через маски/сдвиги, а не через строки.
	level := x & 0b0000_0111
	mode := (x & 0b0001_1000) >> 3

	// Сначала печатаем текущие поля.
	fmt.Printf("%d %d\n", level, mode)

	// TODO: Соберите новый байт x2:
	// 1) Очистите в x только биты полей level и mode (служебные биты не меняйте).
	// 2) Ограничьте newLevel шириной 3 бита, newMode шириной 2 бита.
	// 3) Установите новые значения полей в очищенный байт.
	//
	// Подсказка по требованиям: используйте битовые операторы (&, |, &^, <<, >>).
	level = x &^ 0b0001_1111
	mode = x &^ 0b0001_1111
	newLevel = newLevel & 0b111
	newMode = newMode & 0b011
	cleared := x &^ 0b0001_1111
	x2 := cleared | newLevel | (newMode << 3)

	// Выводим в десятичном и двоичном виде (8 бит).
	fmt.Printf("%d %08b\n", x2, x2)
}
