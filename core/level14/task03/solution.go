package main

import "fmt"

func main() {
	var s string
	fmt.Scan(&s) // по условию читаем одно "слово" (без пробелов)

	if len(s) == 0 {
		// Нельзя трогать s[0] и s[1:], если строка пустая.
		fmt.Print("")
		return
	}

	var b byte = s[0] // первый символ как первый байт

	// TODO: Если первый байт находится в диапазоне 'a'..'z', преобразуйте его в 'A'..'Z' с помощью байтовой арифметики (без strings.ToUpper и аналогов).
	// TODO: Соберите результат как новую строку из первого байта и хвоста исходной строки. Учтите, что s[1:] можно использовать только при len(s) > 0.
	if b >= 'a' && b <= 'z' {
		b = b - ('a' - 'A')
	}
	if len(s) > 0 {
		s = string(b) + s[1:]
	}
	// Временная реализация: выводит строку без нормализации.
	fmt.Print(s)
}
